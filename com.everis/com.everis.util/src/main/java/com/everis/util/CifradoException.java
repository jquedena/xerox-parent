package com.everis.util;

public class CifradoException extends Exception {

    private static final long serialVersionUID = 1L;

    public CifradoException() {
        super();
    }

    public CifradoException(String message, Throwable cause) {
        super(message, cause);
    }

    public CifradoException(String message) {
        super(message);
    }

    public CifradoException(Throwable cause) {
        super(cause);
    }

}
